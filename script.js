// ================= QUIZ LOGIC =================
let questions = [];
let currentQuestion = 0;
let userAnswers = [];
let timer = 300;
let interval;

function startQuiz(subject) {
  questions = [...questionBank[subject]].sort(() => Math.random() - 0.5);
  userAnswers = new Array(questions.length).fill(null);

  document.getElementById("start-screen").classList.add("hide");
  document.getElementById("quiz-screen").classList.remove("hide");

  loadQuestion();
  startTimer();
}

function loadQuestion() {
  const q = questions[currentQuestion];
  document.getElementById("question").innerText = q.question;
  document.getElementById("qno").innerText =
    "Q " + (currentQuestion + 1) + "/" + questions.length;

  const optionsDiv = document.getElementById("options");
  optionsDiv.innerHTML = "";

  q.options.forEach((opt, index) => {
    const btn = document.createElement("div");
    btn.classList.add("option");

    if (userAnswers[currentQuestion] === index) {
      btn.classList.add("selected");
    }

    btn.innerText = opt;

    btn.onclick = () => {
      userAnswers[currentQuestion] = index;
      loadQuestion();
    };

    optionsDiv.appendChild(btn);
  });
}

function nextQuestion() {
  if (currentQuestion < questions.length - 1) {
    currentQuestion++;
    loadQuestion();
  }
}

function prevQuestion() {
  if (currentQuestion > 0) {
    currentQuestion--;
    loadQuestion();
  }
}

function startTimer() {
  interval = setInterval(() => {
    timer--;
    document.getElementById("timer").innerText = "Time: " + timer;

    if (timer <= 0) {
      clearInterval(interval);
      submitQuiz();
    }
  }, 1000);
}

function submitQuiz() {
  clearInterval(interval);

  let score = 0;
  questions.forEach((q, i) => {
    if (userAnswers[i] === q.answer) score++;
  });

  document.getElementById("quiz-screen").classList.add("hide");
  document.getElementById("result-screen").classList.remove("hide");

  document.getElementById("score").innerText =
    "You scored " + score + " out of " + questions.length;
}

function restartQuiz() {
  location.reload();
}